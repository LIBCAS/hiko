                            <label class="flex items-center">
                                <span class="text-sm mr-2">{{ __('hiko.threshold') }}:</span>
                                <input
                                    type="number"
                                    x-model="countryAndNameThreshold"
                                    min="0"
                                    max="100"
                                    class="w-20 px-2 py-1 border rounded text-sm">
                                <span class="text-sm ml-1">%</span>
                            </label>
                        </div>
                    </div>
                </label>
            </div>

            <div class="mt-6 flex justify-center">
                <button
                    type="button"
                    @click="updatePreview()"
                    :disabled="isLoading"
                    class="px-4 py-2 mt-4 bg-gray-800 border border-transparent rounded-md font-semibold text-xs text-white uppercase tracking-widest hover:bg-gray-700 active:bg-gray-900 disabled:opacity-25 disabled:cursor-not-allowed transition ease-in-out duration-150 flex items-center gap-2">
                    <svg x-show="isLoading" class="animate-spin -ml-1 mr-2 h-4 w-4 text-white" xmlns="http://www.w3.org/2000/svg" fill="none" viewBox="0 0 24 24" style="display: none;">
                        <circle class="opacity-25" cx="12" cy="12" r="10" stroke="currentColor" stroke-width="4"></circle>
                        <path class="opacity-75" fill="currentColor" d="M4 12a8 8 0 018-8V0C5.373 0 0 5.373 0 12h4zm2 5.291A7.962 7.962 0 014 12H0c0 3.042 1.135 5.824 3 7.938l3-2.647z"></path>
                    </svg>
                    <span x-text="isLoading ? '{{ __('hiko.loading') }}...' : '{{ __('hiko.update_preview') }}'"></span>
                </button>
