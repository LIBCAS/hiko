                                <span class="text-sm mr-2 w-24">{{ __('hiko.latitude_abbr') }}:</span>
                                <input
                                    type="number"
                                    x-model="latitudeTolerance"
                                    step="0.001"
                                    min="0"
                                    class="w-24 px-2 py-1 border rounded text-sm">
                                <span class="text-sm ml-1">{{ __('hiko.tolerance') }}</span>
                            </label>
