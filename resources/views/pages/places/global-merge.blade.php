<x-app-layout :title="$title">
    <x-success-alert />

    @if($errors->any())
        <div class="max-w-7xl mx-auto mb-4 p-4 bg-red-100 border border-red-400 text-red-700 rounded">
            <h3 class="font-bold mb-2">{{ __('hiko.validation_errors') }}</h3>
            <ul class="list-disc list-inside">
                @foreach($errors->all() as $error)
                    <li>{{ $error }}</li>
                @endforeach
            </ul>
        </div>
    @endif

    @if(session('error'))
        <div class="max-w-7xl mx-auto mb-4 p-4 bg-red-100 border border-red-400 text-red-700 rounded">
            {{ session('error') }}
        </div>
    @endif

    <div class="max-w-7xl mx-auto"
         x-data="{
            criteria: @js($criteria ?? config('global_place_merge.default_criteria')),
            nameSimilarityThreshold: @js($nameSimilarityThreshold ?? config('global_place_merge.name_similarity_threshold')),
            latitudeTolerance: @js($latitudeTolerance ?? config('global_place_merge.latitude_tolerance')),
            longitudeTolerance: @js($longitudeTolerance ?? config('global_place_merge.longitude_tolerance')),
            countryAndNameThreshold: @js($countryAndNameThreshold ?? config('global_place_merge.country_and_name_threshold')),
            isLoading: false,
            submitLoading: false,
            showGuide: false,
            showConfig: @js(request()->has('criteria') ? false : true),

            updatePreview() {
                if (this.criteria.length === 0) {
                    alert(@js(__('hiko.at_least_one_criterion_required')));
                    return;
                }
                this.isLoading = true;
                setTimeout(() => {
                    const params = new URLSearchParams();
                    this.criteria.forEach(c => params.append('criteria[]', c));
                    params.append('name_similarity_threshold', this.nameSimilarityThreshold);
                    params.append('latitude_tolerance', this.latitudeTolerance);
                    params.append('longitude_tolerance', this.longitudeTolerance);
                    params.append('country_and_name_threshold', this.countryAndNameThreshold);
                    window.location.href = '{{ route("places.global-merge") }}?' + params.toString();
                }, 50);
            },

            submitForm() {
                if (this.submitLoading) return;

                if (this.criteria.length === 0) {
                    alert(@js(__('hiko.at_least_one_criterion_required')));
                    return;
                }

                // Check for hidden inputs generated by Livewire
                const selectedPlacesInputs = document.querySelectorAll('input[name=\'selected_places[]\']');
                if (selectedPlacesInputs.length === 0) {
                    alert(@js(__('hiko.at_least_one_place_required')));
                    return;
                }

                if (!confirm(@js(__('hiko.confirm_execute_merge')))) {
                    return;
                }

                this.submitLoading = true;
                this.$refs.mergeForm.submit();
            }
         }"
    >
        <div class="mb-6 flex justify-between">
            <a href="{{ route('places') }}" class="text-sm text-gray-600 hover:text-gray-900 flex items-center gap-1">
                <span>←</span> {{ __('hiko.back_to_places') }}
            </a>
            <button @click="showGuide = !showGuide" type="button" class="text-sm font-semibold text-primary hover:underline flex items-center gap-1">
                <svg class="w-4 h-4" fill="none" stroke="currentColor" viewBox="0 0 24 24"><path stroke-linecap="round" stroke-linejoin="round" stroke-width="2" d="M13 16h-1v-4h-1m1-4h.01M21 12a9 9 0 11-18 0 9 9 0 0118 0z"></path></svg>
                {{ __('hiko.guide') }}
            </button>
        </div>

        <div x-show="showGuide" x-transition class="mb-6 bg-blue-50 border border-blue-200 rounded-md p-4 relative" style="display: none;">
            <button @click="showGuide = false" class="absolute top-2 right-9 text-blue-400 hover:text-blue-600">
                <svg class="w-4 h-4" fill="none" stroke="currentColor" viewBox="0 0 24 24"><path stroke-linecap="round" stroke-linejoin="round" stroke-width="2" d="M6 18L18 6M6 6l12 12"></path></svg>
            </button>
            <h3 class="text-sm font-bold text-blue-800">{{ __('hiko.how_to_merge') }}</h3>
            <ul class="list-disc list-inside text-sm text-blue-700 mt-2">
                {{-- Note: You may want to create specific translation keys for Global Merge steps later --}}
                <li>{{ __('hiko.global_merge_step_1') ?? 'Choose criteria to match local places with existing global places.' }}</li>
                <li>{{ __('hiko.global_merge_step_2') ?? 'Review the preview table. Items marked "Merge" will update a global place. Items marked "Move" will become new global places.' }}</li>
                <li>{{ __('hiko.global_merge_step_3') ?? 'Select the places you wish to process and click "Execute Merge".' }}</li>
            </ul>
        </div>

        {{-- Merging Criteria Configuration --}}
        <div x-show="showConfig" x-transition class="bg-white shadow-sm rounded-lg p-6 mb-6 relative">
            {{-- Loading Overlay for Criteria Update --}}
            <div x-show="isLoading" x-transition.opacity
                 class="absolute inset-0 bg-white bg-opacity-60 z-10 flex items-center justify-center rounded-lg"
                 style="display: none;">
                <div class="flex flex-col items-center p-4 bg-white shadow-lg rounded-full">
                    <svg class="animate-spin h-8 w-8 text-gray-800" xmlns="http://www.w3.org/2000/svg" fill="none" viewBox="0 0 24 24">
